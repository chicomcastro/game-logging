const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const trackValidation = require('../../validations/track.validation');
const trackController = require('../../controllers/track.controller');

const router = express.Router();

router
  .route('/')
  .post(trackController.createTrack)
  .get(auth('getTracks'), validate(trackValidation.getTracks), trackController.getTracks);

router
  .route('/:trackId')
  .get(auth('getTracks'), validate(trackValidation.getTrack), trackController.getTrack)
  .patch(auth('manageTracks'), validate(trackValidation.updateTrack), trackController.updateTrack)
  .delete(auth('manageTracks'), validate(trackValidation.deleteTrack), trackController.deleteTrack);

module.exports = router;

